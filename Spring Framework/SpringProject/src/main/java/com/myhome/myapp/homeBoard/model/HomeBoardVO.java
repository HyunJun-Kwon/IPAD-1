package com.myhome.myapp.homeBoard.model;

public class HomeBoardVO {
	private String admCd;
	private String regionName;
	private String regionNameDetail;
	
	public String getAdmCd() {
		return admCd;
	}
	public void setAdmCd(String admCd) {
		this.admCd = admCd;
	}
	public String getRegionName() {
		return regionName;
	}
	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}
	public String getRegionNameDetail() {
		return regionNameDetail;
	}
	public void setRegionNameDetail(String regionNameDetail) {
		this.regionNameDetail = regionNameDetail;
	}
	
	
}
